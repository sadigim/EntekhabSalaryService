namespace Entekhab.Common.Objects;

public class SysResult
{
    //********************************************************************************************************************
    public SysResult()
    {
        Errors = new List<SysError>();
    }
    //********************************************************************************************************************
    /// <summary>
    /// نتيجه اجرا
    /// </summary>
    public bool Successed { get; set; }
    //********************************************************************************************************************
    /// <summary>
    /// مقدار برگشتي
    /// </summary>
    public object? Value { get; set; }
    //********************************************************************************************************************
    /// <summary>
    /// پيغام برگشتي مرتبط با نتيجه اجرا و مقدار برگشتي تابع
    /// </summary>
    public string Message { get; set; }
    //********************************************************************************************************************
    /// <summary>
    /// مجموعه خطاهاي برگشتي از يک تابع
    /// </summary>
    public IEnumerable<SysError> Errors { get; set; }
    //********************************************************************************************************************
}